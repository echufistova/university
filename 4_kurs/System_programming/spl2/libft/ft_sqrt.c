int	ft_sqrt(int nb)
{
	int c;
	int r;

	c = 1;
	r = 0;
	while (nb > 0)
	{
		nb -= c;
		c = c + 2;
		r++;
	}
	if (nb < 0)
	{
		return (0);
	}
	return (r);
}
